def validate_generated_runtime(outputs: list[Output]) -> None:
    for output in outputs:
        if not isinstance(output.content, str):
            continue
        target = output.target.as_posix()
        lifecycle_text = output.content
        if target.endswith(".toml"):
            try:
                parsed = tomllib.loads(output.content)
            except tomllib.TOMLDecodeError as exc:
                raise SyncError(f"generated TOML is invalid: {output.target}: {exc}") from exc
            if "/.codex/agents/" in target:
                if parsed.get("model") not in SUPPORTED_MODELS:
                    raise SyncError(f"generated agent has no explicit supported model: {output.target}")
                if parsed.get("model_reasoning_effort") not in SUPPORTED_EFFORTS:
                    raise SyncError(f"generated agent has no explicit supported effort: {output.target}")
            lifecycle_text = "\n".join(decoded_text_values(parsed))

        if contains_operative_claude_task_lifecycle(lifecycle_text):
            raise SyncError(
                f"generated runtime contains operative Claude-only Task lifecycle: {output.target}"
            )
        if target.endswith("/.codex/runtime-contract.md") or target.endswith("/.codex/privileged-roles.md"):
            continue
        for label, pattern in FORBIDDEN_GENERATED_PATTERNS:
            if pattern.search(lifecycle_text):
                raise SyncError(f"generated runtime contains forbidden {label}: {output.target}")
        if contains_unsupported_spawn_recipe(lifecycle_text):
            raise SyncError(f"generated runtime contains unsupported spawn recipe: {output.target}")
        if "/.codex/hooks/" in target:
            for pattern in FORBIDDEN_HOOK_PATTERNS:
                if pattern.search(output.content):
                    raise SyncError(f"generated hook contains unsupported runtime assumption: {output.target}")


def build_outputs(project: Path) -> list[Output]:
    required = [project / "CLAUDE.md", project / ".claude/team-lead.md", project / ".claude/consultant-base.md"]
    missing = [str(path) for path in required if not path.is_file()]
    if missing:
        raise SyncError(f"required sources missing: {', '.join(missing)}")
    policy = load_runtime_policy(project)
    outputs = [
        render_agents_md(project),
        render_team_lead(project),
        render_consultant_base(project),
        render_codex_config(project, policy),
        render_runtime_contract(project),
        render_privileged_roles(project),
    ]
    outputs.extend(render_skill_outputs(project))
    outputs.extend(render_agent_outputs(project, policy))
    outputs.extend(render_command_outputs(project))
    outputs.extend(render_hook_outputs(project))
    targets = [str(output.target.relative_to(project)) for output in outputs]
    if len(targets) != len(set(targets)):
        raise SyncError("two sources generate the same Codex target")
    for output in outputs:
        relative = relative_inside(output.target, project)
        if relative != Path("AGENTS.md") and relative.parts[:1] not in ((".codex",), (".agents",)):
            raise SyncError(f"generated target outside allowlist: {output.target}")
        if isinstance(output.content, str) and contains_secret(output.content):
            raise SyncError(f"secret-looking generated content rejected: {output.target}")
    validate_generated_runtime(outputs)
    return outputs


def validate_manifest_target(relative: str) -> None:
    posix = PurePosixPath(relative)
    windows = PureWindowsPath(relative)
    canonical = posix.as_posix()
    codex_exact_targets = {
        ".codex/config.toml",
        ".codex/consultant-base.md",
        ".codex/runtime-contract.md",
        ".codex/privileged-roles.md",
        ".codex/hooks.json",
        ".codex/hooks/checkpoint_context.py",
        ".codex/hooks/SessionStart.sh",
        ".codex/hooks/post-compact-restore.sh",
    }
    codex_agent_target = (
        len(posix.parts) == 3
        and posix.parts[:2] == (".codex", "agents")
        and posix.suffix == ".toml"
    )
    agents_skill_target = (
        len(posix.parts) >= 4
        and posix.parts[:2] == (".agents", "skills")
        and bool(SLUG_RE.fullmatch(posix.parts[2]))
    )
    managed = (
        relative == "AGENTS.md"
        or agents_skill_target
        or relative in codex_exact_targets
        or codex_agent_target
    )
    if (
        not relative
        or "\\" in relative
        or posix.is_absolute()
        or windows.is_absolute()
        or canonical != relative
        or ".." in posix.parts
        or not managed
    ):
        raise SyncError(f"manifest output target is not managed: {relative!r}")


def manifest_entry_matches_target(relative: str, source: str, adapter: str) -> bool:
    target = PurePosixPath(relative)
    if relative == "AGENTS.md":
        return source == "CLAUDE.md" and adapter == "project-instructions"
    if relative == ".agents/skills/team-lead/SKILL.md":
        return source == ".claude/team-lead.md" and adapter == "team-lead"
    if target.parts[:2] == (".agents", "skills"):
        source_path = PurePosixPath(source)
        if target.parts[2].startswith("source-command-"):
            command = target.parts[2].removeprefix("source-command-")
            return (
                target.parts[3:] == ("SKILL.md",)
                and source == f".claude/commands/{command}.md"
                and adapter == "command-skill"
            )
        expected_source = PurePosixPath(".claude", "skills", *target.parts[2:])
        return source_path == expected_source and adapter in {"skill", "skill-asset"}
    if target.parts[:2] == (".codex", "agents"):
        expected_source = f".claude/agents/{target.stem}.md"
        return source == expected_source and adapter == "agent"
    expected = {
        ".codex/config.toml": (".claude/team-lead.md", "codex-config"),
        ".codex/consultant-base.md": (
            ".claude/consultant-base.md",
            "shared-agent-contract",
        ),
        ".codex/runtime-contract.md": (
            ".claude/codex/runtime-contract.md",
            "runtime-contract",
        ),
        ".codex/privileged-roles.md": (
            ".claude/codex/privileged-roles.md",
            "privileged-roles",
        ),
        ".codex/hooks.json": (".claude/hooks", "hooks-config"),
        ".codex/hooks/checkpoint_context.py": (".claude/hooks", "hook"),
        ".codex/hooks/SessionStart.sh": (".claude/hooks/SessionStart.sh", "hook"),
        ".codex/hooks/post-compact-restore.sh": (
            ".claude/hooks/post-compact-restore.sh",
            "hook",
        ),
    }
    return expected.get(relative) == (source, adapter)


def validate_manifest_entry(relative: str, entry: object) -> None:
    validate_manifest_target(relative)
    required = {"source", "adapter", "source_sha256", "output_sha256"}
    if not isinstance(entry, dict) or set(entry) != required:
        raise SyncError(f"invalid manifest output entry: {relative}")
    if not isinstance(entry["source"], str) or not entry["source"]:
        raise SyncError(f"invalid manifest output entry source: {relative}")
    if not isinstance(entry["adapter"], str) or not entry["adapter"]:
        raise SyncError(f"invalid manifest output entry adapter: {relative}")
    if not manifest_entry_matches_target(relative, entry["source"], entry["adapter"]):
        raise SyncError(f"manifest output target is not managed: {relative!r}")
    for field in ("source_sha256", "output_sha256"):
        value = entry[field]
        if not isinstance(value, str) or not SHA256_RE.fullmatch(value):
            raise SyncError(f"invalid manifest output entry {field}: {relative}")


def load_manifest(project: Path) -> dict:
    path = project / MANIFEST_REL
    if not path.exists():
        return {
            "version": VERSION,
            "generator": MANIFEST_GENERATOR,
            "outputs": {},
        }
    reject_symlink(path, project)
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, UnicodeDecodeError) as exc:
        raise SyncError(f"invalid sync manifest: {path}") from exc
    if not isinstance(data, dict) or set(data) != {"version", "generator", "outputs"}:
        raise SyncError(f"invalid sync manifest schema: {path}")
    if data["version"] != VERSION:
        raise SyncError(f"unsupported sync manifest version: {data['version']!r}")
    if data["generator"] != MANIFEST_GENERATOR:
        raise SyncError(f"unexpected sync manifest generator: {data['generator']!r}")
    if not isinstance(data["outputs"], dict):
        raise SyncError(f"invalid outputs map in sync manifest: {path}")
    for relative, entry in data["outputs"].items():
        if not isinstance(relative, str):
            raise SyncError("manifest output key must be a string")
        validate_manifest_entry(relative, entry)
    return data


def plan_desired_actions(
    project: Path,
    desired: dict[str, Output],
    previous: dict,
    force: bool,
) -> list[PlannedAction]:
    actions: list[PlannedAction] = []
    for relative, output in sorted(desired.items()):
        target = output.target
        if not target.exists():
            actions.append(PlannedAction("create", output, target))
            continue
        reject_symlink(target, project)
        current = target.read_bytes()
        if current == content_bytes(output.content):
            actions.append(PlannedAction("unchanged", output, target))
            continue
        previous_hash = previous.get(relative, {}).get("output_sha256")
        if not force and (not previous_hash or digest(current) != previous_hash):
            actions.append(PlannedAction("conflict", output, target, "generated target was edited or is unmanaged"))
        else:
            actions.append(PlannedAction("update", output, target))
    return actions


def plan_stale_actions(
    project: Path,
    desired: dict[str, Output],
    previous: dict,
    prune: bool,
    force: bool,
) -> list[PlannedAction]:
    actions: list[PlannedAction] = []
    for relative, entry in sorted(previous.items()):
        if relative in desired:
            continue
        target = project / Path(relative)
        if not target.exists():
            continue
        reject_symlink(target, project)
        current_hash = digest(target.read_bytes())
        if current_hash != entry.get("output_sha256"):
            if force and prune:
                actions.append(
                    PlannedAction(
                        "delete",
                        None,
                        target,
                        "forced prune of edited stale generated target",
                    )
                )
            else:
                actions.append(
                    PlannedAction(
                        "conflict",
                        None,
                        target,
                        "stale generated target was edited",
                    )
                )
        elif prune:
            actions.append(PlannedAction("delete", None, target, "source removed"))
        else:
            actions.append(PlannedAction("stale", None, target, "source removed; rerun with --prune"))
    return actions


def plan_actions(
    project: Path,
    outputs: list[Output],
    prune: bool,
    force: bool,
) -> list[PlannedAction]:
    previous = load_manifest(project).get("outputs", {})
    desired = {
        str(output.target.relative_to(project)).replace("\\", "/"): output
        for output in outputs
    }
    return plan_desired_actions(project, desired, previous, force) + plan_stale_actions(
        project,
        desired,
        previous,
        prune,
        force,
    )


def atomic_write(path: Path, content: str | bytes) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    descriptor, temporary_name = tempfile.mkstemp(prefix=f".{path.name}.", dir=path.parent)
    try:
        with os.fdopen(descriptor, "wb") as handle:
            handle.write(content_bytes(content))
            handle.flush()
            os.fsync(handle.fileno())
        os.replace(temporary_name, path)
    finally:
        temporary = Path(temporary_name)
        if temporary.exists():
            temporary.unlink()


def process_alive(pid: int) -> bool:
    try:
        os.kill(pid, 0)
    except ProcessLookupError:
        return False
    except PermissionError:
        return True
    return True


def stale_lock(lock: Path) -> bool:
    try:
        raw = lock.read_text(encoding="utf-8").strip()
        pid = int(raw)
    except (OSError, UnicodeDecodeError, ValueError):
        return True
    return pid == os.getpid() or not process_alive(pid)


def acquire_lock(project: Path) -> Path:
    lock = project / LOCK_REL
    lock.parent.mkdir(parents=True, exist_ok=True)
    reject_symlink(lock.parent, project)
    for attempt in range(2):
        try:
            descriptor = os.open(lock, os.O_CREAT | os.O_EXCL | os.O_WRONLY)
            break
        except FileExistsError as exc:
            if attempt == 0 and stale_lock(lock):
                lock.unlink()
                continue
            raise SyncError(f"another Codex sync may be running: {lock}") from exc
    else:  # pragma: no cover - loop either breaks or raises
        raise SyncError(f"could not acquire Codex sync lock: {lock}")
    with os.fdopen(descriptor, "w", encoding="utf-8") as handle:
        handle.write(str(os.getpid()))
    return lock


def write_manifest(project: Path, outputs: list[Output]) -> None:
    entries = {}
    for output in sorted(outputs, key=lambda item: str(item.target)):
        relative = str(output.target.relative_to(project)).replace("\\", "/")
        source_path = project / output.source
        source_content = read_source_bytes(source_path, project) if source_path.is_file() else b""
        entries[relative] = {
            "source": output.source.replace("\\", "/"),
            "adapter": output.adapter,
            "source_sha256": digest(source_content),
            "output_sha256": digest(output.content),
        }
    manifest = {
        "version": VERSION,
        "generator": MANIFEST_GENERATOR,
        "outputs": entries,
    }
    atomic_write(project / MANIFEST_REL, json.dumps(manifest, ensure_ascii=False, indent=2, sort_keys=True) + "\n")


def _reconcile_manifest_to_disk(project: Path, outputs: list[Output], previous: dict) -> None:
    """Rebuild MANIFEST_REL from what is ACTUALLY on disk right now (F11 fix).

    Recovery path for a partial apply_actions() failure: the write loop below
    may die partway through (disk full, permission error, process killed),
    leaving some targets already rewritten to their NEW content while the
    manifest still holds the OLD pre-run hashes (write_manifest() only runs
    once, after the loop fully completes). On the next plan_actions() call,
    that stale manifest makes a file THIS SAME crashed run already rewrote
    look like it was "independently edited" (on-disk hash != manifest hash)
    — a phantom conflict that then demands --force, even though no human
    touched anything.

    Fix: after any exception during apply, re-derive every entry from the
    union of previously-tracked keys and newly-desired keys by hashing each
    target's REAL current bytes (not the previous manifest's hash, not the
    newly-desired-but-maybe-unwritten content). A target that no longer
    exists (successfully deleted before the crash) is dropped. A target
    still pending delete (crash happened before its turn) keeps its
    previous metadata with a refreshed hash, so the next run's
    plan_stale_actions() still sees it as owned-but-undesired and re-queues
    the delete — it is not silently orphaned from manifest tracking.
    """
    desired_by_key: dict[str, Output] = {
        str(output.target.relative_to(project)).replace("\\", "/"): output
        for output in outputs
    }
    entries: dict[str, dict] = {}
    for key in sorted(set(desired_by_key) | set(previous)):
        target = project / Path(key)
        if not target.exists():
            continue  # deleted (or never created) — must not appear in the manifest
        entries[key] = _reconciled_entry(project, target, desired_by_key.get(key), previous.get(key, {}))
    manifest = {"version": VERSION, "generator": MANIFEST_GENERATOR, "outputs": entries}
    atomic_write(project / MANIFEST_REL, json.dumps(manifest, ensure_ascii=False, indent=2, sort_keys=True) + "\n")


def _reconciled_entry(project: Path, target: Path, output: Output | None, previous_entry: dict) -> dict:
    """One manifest entry, hashed from `target`'s REAL current bytes.

    Auditor M1: sibling planners (plan_desired_actions/plan_stale_actions)
    both guard their target.read_bytes() with reject_symlink() — this
    recovery path must too. A SyncError raised here degrades correctly: the
    caller's best-effort except (apply_actions()) swallows it and re-raises
    the ORIGINAL crash exception.
    """
    reject_symlink(target, project)
    current_hash = digest(target.read_bytes())
    if output is None:
        return {**previous_entry, "output_sha256": current_hash}
    source_path = project / output.source
    source_content = read_source_bytes(source_path, project) if source_path.is_file() else b""
    return {
        "source": output.source.replace("\\", "/"),
        "adapter": output.adapter,
        "source_sha256": digest(source_content),
        "output_sha256": current_hash,
    }


def apply_actions(project: Path, outputs: list[Output], actions: list[PlannedAction]) -> None:
    stale = [action for action in actions if action.action == "stale"]
    if stale:
        paths = ", ".join(str(action.target.relative_to(project)) for action in stale)
        raise SyncError(
            "stale managed outputs require pruning before apply; "
            f"rerun with --apply --prune: {paths}"
        )
    conflicts = [action for action in actions if action.action == "conflict"]
    if conflicts:
        paths = ", ".join(str(action.target.relative_to(project)) for action in conflicts)
        raise ConflictError(f"refusing to overwrite independently edited targets: {paths}")
    lock = acquire_lock(project)
    try:
        try:
            for action in actions:
                if action.action in {"create", "update"}:
                    assert action.output is not None
                    atomic_write(action.target, action.output.content)
                elif action.action == "delete":
                    action.target.unlink()
                    parent = action.target.parent
                    while parent != project and parent != project / ".codex" and parent.exists():
                        try:
                            parent.rmdir()
                        except OSError:
                            break
                        parent = parent.parent
            write_manifest(project, outputs)
        except BaseException:
            # F11 fix: a partial failure must not leave a stale manifest
            # behind — reconcile it to physical reality (best-effort; the
            # ORIGINAL exception always propagates regardless of whether
            # this recovery write itself succeeds).
            try:
                _reconcile_manifest_to_disk(project, outputs, load_manifest(project).get("outputs", {}))
            except Exception:
                pass
            raise
    finally:
        lock.unlink(missing_ok=True)


def report(project: Path, actions: list[PlannedAction]) -> None:
    counts: dict[str, int] = {}
    for action in actions:
        counts[action.action] = counts.get(action.action, 0) + 1
    summary = ", ".join(f"{key}={counts[key]}" for key in sorted(counts))
    print(f"sync-to-codex: {summary or 'no outputs'}")
    for action in actions:
        if action.action not in {"unchanged"}:
            suffix = f" ({action.reason})" if action.reason else ""
            print(f"  {action.action}: {action.target.relative_to(project)}{suffix}")


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    mode = parser.add_mutually_exclusive_group()
    mode.add_argument("--apply", action="store_true", help="Write generated runtime files.")
    mode.add_argument("--check", action="store_true", help="Exit 1 when generated runtime has drift.")
    parser.add_argument("--project", type=Path, default=Path.cwd(), help="Project root. Default: current directory.")
    parser.add_argument("--prune", action="store_true", help="Delete stale manifest-owned outputs.")
    parser.add_argument("--force", action="store_true", help="Overwrite unmanaged or independently edited generated targets.")
    parser.add_argument("--quiet", action="store_true")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv or sys.argv[1:])
    project = args.project.expanduser().resolve()
    if not project.is_dir():
        print(f"error: project root not found: {project}", file=sys.stderr)
        return 2
    try:
        outputs = build_outputs(project)
        actions = plan_actions(project, outputs, prune=args.prune, force=args.force)
        if not args.quiet:
            report(project, actions)
        if args.apply:
            apply_actions(project, outputs, actions)
            if not args.quiet:
                print("Codex runtime generated successfully.")
            return 0
        drift = any(action.action not in {"unchanged"} for action in actions)
        if args.check and drift:
            return 1
        return 0
    except SyncError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return exc.exit_code


if __name__ == "__main__":
    raise SystemExit(main())
