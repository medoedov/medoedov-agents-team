from pathlib import Path


VERSION = 1
MANIFEST_REL = Path(".codex/.sync/claude-to-codex.json")
LOCK_REL = Path(".codex/.sync/claude-to-codex.lock")
SLUG_RE = re.compile(r"^[a-z0-9][a-z0-9-]{0,62}[a-z0-9]$|^[a-z0-9]$")
SECRET_PATTERNS = (
    re.compile(r"-----BEGIN [A-Z ]*PRIVATE KEY-----"),
    re.compile(r"(?<![A-Za-z0-9])gh[pousr]_[A-Za-z0-9_]{20,}"),
    re.compile(r"(?<![A-Za-z0-9])sk-[A-Za-z0-9_-]{20,}"),
    re.compile(r"\bAKIA[0-9A-Z]{16}\b"),
    re.compile(r"\b[0-9]{8,10}:[A-Za-z0-9_-]{30,}\b"),
)
WRITE_TOOLS = {"edit", "multiedit", "notebookedit", "write"}
WRITE_BY_ROLE = {"watchdog"}
GENERATED_MARKER = "Generated by sync_to_codex.py. Do not edit directly."


class SyncError(Exception):
    """Base error for safe, user-actionable sync failures."""

    exit_code = 2


class ConflictError(SyncError):
    """A generated target was edited independently."""

    exit_code = 3


@dataclass(frozen=True)
class Output:
    source: str
    target: Path
    adapter: str
    content: str | bytes


@dataclass(frozen=True)
class PlannedAction:
    action: str
    output: Output | None
    target: Path
    reason: str = ""


def content_bytes(content: str | bytes) -> bytes:
    return content.encode("utf-8") if isinstance(content, str) else content


def digest(content: str | bytes) -> str:
    return hashlib.sha256(content_bytes(content)).hexdigest()


def validate_slug(value: str, label: str) -> None:
    if not SLUG_RE.fullmatch(value):
        raise SyncError(f"invalid {label} slug: {value!r}")


def relative_inside(path: Path, root: Path) -> Path:
    try:
        return path.resolve().relative_to(root.resolve())
    except ValueError as exc:
        raise SyncError(f"path escapes project root: {path}") from exc


def reject_symlink(path: Path, root: Path) -> None:
    relative = relative_inside(path, root)
    current = root.resolve()
    for part in relative.parts:
        current /= part
        if current.is_symlink():
            raise SyncError(f"symlink source rejected: {path}")


def contains_secret(text: str) -> bool:
    return any(pattern.search(text) for pattern in SECRET_PATTERNS)


def read_source_bytes(path: Path, root: Path) -> bytes:
    reject_symlink(path, root)
    raw = path.read_bytes()
    if contains_secret(raw.decode("utf-8", errors="ignore")):
        raise SyncError(f"secret-looking content rejected: {path}")
    return raw


def read_source(path: Path, root: Path) -> str:
    try:
        text = read_source_bytes(path, root).decode("utf-8")
    except UnicodeDecodeError as exc:
        raise SyncError(f"non-UTF-8 source rejected: {path}") from exc
    if contains_secret(text):
        raise SyncError(f"secret-looking content rejected: {path}")
    return text


def split_frontmatter(text: str) -> tuple[dict[str, str | list[str]], str]:
    """Parse the small YAML subset used by Claude agent and skill files."""
    lines = text.splitlines(keepends=True)
    if not lines or lines[0].strip() != "---":
        return {}, text
    end = next((index for index, line in enumerate(lines[1:], 1) if line.strip() == "---"), None)
    if end is None:
        raise SyncError("unterminated YAML frontmatter")

    raw = [line.rstrip("\r\n") for line in lines[1:end]]
    metadata: dict[str, str | list[str]] = {}
    index = 0
    while index < len(raw):
        line = raw[index]
        if not line.strip():
            index += 1
            continue
        match = re.match(r"^([A-Za-z0-9_-]+):\s*(.*)$", line)
        if not match:
            index += 1
            continue
        key, value = match.group(1), match.group(2).strip()
        if value in {"|", ">"}:
            index += 1
            block: list[str] = []
            while index < len(raw) and (not raw[index].strip() or raw[index][0].isspace()):
                block.append(raw[index].strip())
                index += 1
            metadata[key] = "\n".join(block).strip()
            continue
        if not value:
            index += 1
            values: list[str] = []
            while index < len(raw):
                item = re.match(r"^\s+-\s+(.+)$", raw[index])
                if not item:
                    break
                values.append(item.group(1).strip().strip("'\""))
                index += 1
            metadata[key] = values
            continue
        metadata[key] = value.strip("'\"")
        index += 1
    return metadata, "".join(lines[end + 1 :]).lstrip("\r\n")


def metadata_list(value: str | list[str] | None) -> list[str]:
    if value is None:
        return []
    if isinstance(value, list):
        return [item.strip() for item in value if item.strip()]
    stripped = value.strip()
    if stripped.startswith("[") and stripped.endswith("]"):
        stripped = stripped[1:-1]
    return [item.strip().strip("'\"") for item in stripped.split(",") if item.strip()]


def markdown_header(source: str) -> str:
    return f"<!-- {GENERATED_MARKER} Source: {source}. -->\n"


def toml_string(value: str) -> str:
    """Return a single-line TOML basic string and avoid multiline injection."""
    replacements = {
        "\\": "\\\\",
        '"': '\\"',
        "\b": "\\b",
        "\t": "\\t",
        "\n": "\\n",
        "\f": "\\f",
        "\r": "\\r",
    }
    encoded = "".join(replacements.get(char, char) for char in value)
    return f'"{encoded}"'


def serialize_agent(fields: dict[str, str]) -> str:
    required = {"name", "description", "developer_instructions", "sandbox_mode"}
    allowed = required | {"model_reasoning_effort"}
    if not required.issubset(fields) or set(fields) - allowed:
        raise SyncError(f"unexpected Codex agent fields: {sorted(set(fields) - allowed)}")
    content = "\n".join(f"{key} = {toml_string(value)}" for key, value in fields.items()) + "\n"
    parsed = tomllib.loads(content)
    if parsed != fields:
        raise SyncError("Codex agent TOML failed round-trip validation")
    return f"# {GENERATED_MARKER}\n{content}"


