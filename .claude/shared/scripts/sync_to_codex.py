def content_bytes(content: str | bytes) -> bytes:
    return content.encode("utf-8") if isinstance(content, str) else content


def digest(content: str | bytes) -> str:
    return hashlib.sha256(content_bytes(content)).hexdigest()


def validate_slug(value: str, label: str) -> None:
    if not SLUG_RE.fullmatch(value):
        raise SyncError(f"invalid {label} slug: {value!r}")


def relative_inside(path: Path, root: Path) -> Path:
    try:
        return path.resolve().relative_to(root.resolve())
    except ValueError as exc:
        raise SyncError(f"path escapes project root: {path}") from exc


def reject_symlink(path: Path, root: Path) -> None:
    relative = relative_inside(path, root)
    current = root.resolve()
    for part in relative.parts:
        current /= part
        if current.is_symlink():
            raise SyncError(f"symlink source rejected: {path}")


def contains_secret(text: str) -> bool:
    return any(pattern.search(text) for pattern in SECRET_PATTERNS)


def scan_quoted_string(text: str, start: int) -> int:
    quote = text[start]
    delimiter = quote * 3 if text.startswith(quote * 3, start) else quote
    index = start + len(delimiter)
    while index < len(text):
        if text[index] == "\\":
            index += 2
            continue
        if text.startswith(delimiter, index):
            return index + len(delimiter)
        index += 1
    return len(text)


def find_balanced_call_end(text: str, opening: int) -> int:
    depth = 0
    index = opening
    while index < len(text):
        if text[index] in {"'", '"'}:
            index = scan_quoted_string(text, index)
            continue
        if text[index] == "(":
            depth += 1
        elif text[index] == ")":
            depth -= 1
            if depth == 0:
                return index + 1
        index += 1
    return len(text)


def find_named_calls(text: str) -> list[tuple[str, int, int]]:
    calls: list[tuple[str, int, int]] = []
    index = 0
    while index < len(text):
        if text[index] in {"'", '"'}:
            index = scan_quoted_string(text, index)
            continue
        if not (text[index].isalpha() or text[index] == "_"):
            index += 1
            continue
        end = index + 1
        while end < len(text) and (text[end].isalnum() or text[end] == "_"):
            end += 1
        name = text[index:end]
        opening = end
        while opening < len(text) and text[opening].isspace():
            opening += 1
        if name in {"Task", "spawn_agent"} and text[opening:opening + 1] == "(":
            calls.append((name, index, find_balanced_call_end(text, opening)))
        index = end
    return calls


def call_tokens(text: str) -> list[tuple[str, str]]:
    tokens: list[tuple[str, str]] = []
    index = 0
    while index < len(text):
        if text[index].isspace():
            index += 1
            continue
        if text[index] in {"'", '"'}:
            end = scan_quoted_string(text, index)
            width = 3 if text.startswith(text[index] * 3, index) else 1
            tokens.append(("string", text[index + width : end - width]))
            index = end
            continue
        if text[index].isalpha() or text[index] == "_":
            end = index + 1
            while end < len(text) and (
                text[end].isalnum() or text[end] in {"_", "-"}
            ):
                end += 1
            tokens.append(("identifier", text[index:end]))
            index = end
            continue
        tokens.append(("punctuation", text[index]))
        index += 1
    return tokens


def has_named_argument(tokens: list[tuple[str, str]], names: set[str]) -> bool:
    for index, token in enumerate(tokens[:-1]):
        if token[0] != "identifier" or token[1].lower() not in names:
            continue
        if tokens[index + 1] == ("punctuation", "=") or tokens[index + 1] == (
            "punctuation",
            ":",
        ):
            return True
    return False


def has_general_purpose_agent_type(tokens: list[tuple[str, str]]) -> bool:
    for index, token in enumerate(tokens[:-2]):
        if token[0] != "identifier" or token[1].lower() != "agent_type":
            continue
        if tokens[index + 1] not in {
            ("punctuation", "="),
            ("punctuation", ":"),
        }:
            continue
        value_type, value = tokens[index + 2]
        if value_type in {"identifier", "string"} and value.lower() == "general-purpose":
            return True
    return False


def operative_calls(text: str) -> list[tuple[str, str]]:
    calls: list[tuple[str, str]] = []
    suppressed_until = -1
    for name, start, end in find_named_calls(text):
        if start < suppressed_until:
            continue
        prefix = text[max(0, start - 200) : start]
        if NEGATIVE_TASK_LIFECYCLE_PREFIX.search(prefix):
            suppressed_until = end
            continue
        opening = text.find("(", start, end)
        calls.append((name, text[opening + 1 : max(opening + 1, end - 1)]))
    return calls


def contains_operative_claude_task_lifecycle(text: str) -> bool:
    for name, arguments in operative_calls(text):
        if name == "Task" and has_named_argument(
            call_tokens(arguments),
            {"subagent_type"},
        ):
            return True
    return False


def contains_unsupported_spawn_recipe(text: str) -> bool:
    for name, arguments in operative_calls(text):
        if name != "spawn_agent":
            continue
        tokens = call_tokens(arguments)
        if has_named_argument(tokens, {"subagent_type", "team_name"}):
            return True
        if has_general_purpose_agent_type(tokens):
            return True
    return False


def decoded_text_values(value: object) -> list[str]:
    if isinstance(value, str):
        return [value]
    if isinstance(value, dict):
        return [
            text
            for nested in value.values()
            for text in decoded_text_values(nested)
        ]
    if isinstance(value, list):
        return [text for nested in value for text in decoded_text_values(nested)]
    return []


def read_source_bytes(path: Path, root: Path) -> bytes:
    reject_symlink(path, root)
    raw = path.read_bytes()
    if contains_secret(raw.decode("utf-8", errors="ignore")):
        raise SyncError(f"secret-looking content rejected: {path}")
    return raw


def read_source(path: Path, root: Path) -> str:
    try:
        text = read_source_bytes(path, root).decode("utf-8")
    except UnicodeDecodeError as exc:
        raise SyncError(f"non-UTF-8 source rejected: {path}") from exc
    if contains_secret(text):
        raise SyncError(f"secret-looking content rejected: {path}")
    return text


def split_frontmatter(text: str) -> tuple[dict[str, str | list[str]], str]:
    """Parse the small YAML subset used by Claude agent and skill files."""
    lines = text.splitlines(keepends=True)
    if not lines or lines[0].strip() != "---":
        return {}, text
    end = next((index for index, line in enumerate(lines[1:], 1) if line.strip() == "---"), None)
    if end is None:
        raise SyncError("unterminated YAML frontmatter")

    raw = [line.rstrip("\r\n") for line in lines[1:end]]
    metadata: dict[str, str | list[str]] = {}
    index = 0
    while index < len(raw):
        line = raw[index]
        if not line.strip():
            index += 1
            continue
        match = re.match(r"^([A-Za-z0-9_-]+):\s*(.*)$", line)
        if not match:
            index += 1
            continue
        key, value = match.group(1), match.group(2).strip()
        if value in {"|", ">"}:
            index += 1
            block: list[str] = []
            while index < len(raw) and (not raw[index].strip() or raw[index][0].isspace()):
                block.append(raw[index].strip())
                index += 1
            metadata[key] = "\n".join(block).strip()
            continue
        if not value:
            index += 1
            values: list[str] = []
            while index < len(raw):
                item = re.match(r"^\s+-\s+(.+)$", raw[index])
                if not item:
                    break
                values.append(item.group(1).strip().strip("'\""))
                index += 1
            metadata[key] = values
            continue
        metadata[key] = value.strip("'\"")
        index += 1
    return metadata, "".join(lines[end + 1 :]).lstrip("\r\n")


def metadata_list(value: str | list[str] | None) -> list[str]:
    if value is None:
        return []
    if isinstance(value, list):
        return [item.strip() for item in value if item.strip()]
    stripped = value.strip()
    if stripped.startswith("[") and stripped.endswith("]"):
        stripped = stripped[1:-1]
    return [item.strip().strip("'\"") for item in stripped.split(",") if item.strip()]


def markdown_header(source: str) -> str:
    return f"<!-- {GENERATED_MARKER} Source: {source}. -->\n"


def toml_string(value: str) -> str:
    """Return a single-line TOML basic string and avoid multiline injection."""
    replacements = {
        "\\": "\\\\",
        '"': '\\"',
        "\b": "\\b",
        "\t": "\\t",
        "\n": "\\n",
        "\f": "\\f",
        "\r": "\\r",
    }
    encoded = "".join(replacements.get(char, char) for char in value)
    return f'"{encoded}"'


def serialize_agent(fields: dict[str, str]) -> str:
    required = {"name", "description", "developer_instructions", "sandbox_mode"}
    allowed = required | {"model", "model_reasoning_effort"}
    if not required.issubset(fields) or set(fields) - allowed:
        raise SyncError(f"unexpected Codex agent fields: {sorted(set(fields) - allowed)}")
    content = "\n".join(f"{key} = {toml_string(value)}" for key, value in fields.items()) + "\n"
    parsed = tomllib.loads(content)
    if parsed != fields:
        raise SyncError("Codex agent TOML failed round-trip validation")
    return f"# {GENERATED_MARKER}\n{content}"


def validate_runtime_table(runtime: object) -> dict[str, int | str]:
    if not isinstance(runtime, dict):
        raise SyncError("Codex runtime policy requires a [runtime] table")
    if set(runtime) != {"max_threads", "max_depth", "default_fork_turns"}:
        raise SyncError("Codex runtime policy requires max_threads, max_depth, and default_fork_turns")
    max_threads = runtime.get("max_threads")
    max_depth = runtime.get("max_depth")
    default_fork_turns = runtime.get("default_fork_turns")
    if type(max_threads) is not int or not 1 <= max_threads <= MAX_CONFIGURED_THREADS:
        raise SyncError(
            f"Codex runtime max_threads must be an integer from 1 to {MAX_CONFIGURED_THREADS}"
        )
    if type(max_depth) is not int or max_depth != 1:
        raise SyncError("Codex runtime max_depth must be integer 1")
    if not isinstance(default_fork_turns, str) or default_fork_turns != "none":
        raise SyncError('Codex runtime default_fork_turns must be "none"')
    return {
        "max_threads": max_threads,
        "max_depth": max_depth,
        "default_fork_turns": default_fork_turns,
    }


def validate_profile_coverage(project: Path, profiles: object) -> dict[str, object]:
    if not isinstance(profiles, dict):
        raise SyncError("Codex runtime policy requires [profiles.<role>] tables")
    source_agents = {path.stem for path in (project / ".claude/agents").glob("*.md")}
    profile_names = set(profiles)
    missing = sorted(source_agents - profile_names)
    extra = sorted(profile_names - source_agents)
    if missing:
        raise SyncError(f"agent profile coverage missing: {', '.join(missing)}")
    if extra:
        raise SyncError(f"agent profile coverage has extra names: {', '.join(extra)}")
    return profiles


def normalize_agent_profile(name: str, profile: object) -> dict[str, str]:
    if not isinstance(profile, dict):
        raise SyncError(f"agent profile must be a table: {name}")
    if set(profile) != {"model", "effort", "source"}:
        raise SyncError(f"agent profile requires model, effort, and source: {name}")
    model, effort, source = profile.get("model"), profile.get("effort"), profile.get("source")
    if not isinstance(model, str) or model not in SUPPORTED_MODELS:
        raise SyncError(f"unsupported model profile for agent {name}: {model}")
    if not isinstance(effort, str) or effort not in SUPPORTED_EFFORTS:
        raise SyncError(f"unsupported reasoning effort for agent {name}: {effort}")
    expected_source = f".claude/agents/{name}.md"
    if not isinstance(source, str) or Path(source).as_posix() != expected_source:
        raise SyncError(f"agent profile source must be {expected_source}: {name}")
    return {"model": model, "effort": effort, "source": source}


def load_runtime_policy(project: Path) -> RuntimePolicy:
    path = project / POLICY_REL
    if not path.is_file():
        raise SyncError(f"Codex runtime policy missing: {path}")
    reject_symlink(path, project)
    try:
        data = tomllib.loads(read_source(path, project))
    except tomllib.TOMLDecodeError as exc:
        raise SyncError(f"invalid Codex runtime policy: {path}: {exc}") from exc
    runtime = validate_runtime_table(data.get("runtime"))
    profiles = validate_profile_coverage(project, data.get("profiles"))
    normalized = {
        name: normalize_agent_profile(name, profile)
        for name, profile in profiles.items()
    }
    return RuntimePolicy(runtime=runtime, profiles=normalized)


