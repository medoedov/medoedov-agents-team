"""Generate a Codex runtime from the Claude-side agent-system sources.

The editable source of truth is CLAUDE.md plus .claude/**. AGENTS.md and
.agents/** and managed .codex/** files are generated outputs and must not be edited directly.
"""

from __future__ import annotations

