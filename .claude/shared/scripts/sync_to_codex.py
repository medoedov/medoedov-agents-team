    outputs: list[Output] = []
    if not source_root.is_dir():
        return outputs
    for source in sorted(source_root.glob("*.md")):
        metadata, body = split_frontmatter(read_source(source, project))
        name, description = metadata.get("name"), metadata.get("description")
        if not isinstance(name, str) or not isinstance(description, str) or not name or not description:
            raise SyncError(f"agent frontmatter requires name and description: {source}")
        validate_slug(name, "agent name")
